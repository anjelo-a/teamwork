import {
  createAuthHeaders,
  getEnvInteger,
  getEnvString,
  requestJson,
  stripTrailingSlash,
  toIsoTimestamp,
} from './perf-utils.mjs';
import { fileURLToPath } from 'node:url';

const DEFAULT_API_BASE_URL = 'http://127.0.0.1:3000';
const DEFAULT_BENCH_EMAIL = 'bench.user@teamwork.local';
const DEFAULT_BENCH_PASSWORD = 'TeamworkBench123!';
const DEFAULT_BENCH_DISPLAY_NAME = 'Bench User';
const DEFAULT_BENCH_WORKSPACE_NAME = 'TeamWork Benchmark Workspace';
const DEFAULT_BENCH_TASK_COUNT = 200;
const DEFAULT_BENCH_TASK_PREFIX = '[bench]';

export async function prepareFixtures(options = {}) {
  const apiBaseUrl = stripTrailingSlash(
    options.apiBaseUrl ?? getEnvString('API_BASE_URL', DEFAULT_API_BASE_URL),
  );
  const benchEmail = options.benchEmail ?? getEnvString('BENCH_EMAIL', DEFAULT_BENCH_EMAIL);
  const benchPassword =
    options.benchPassword ?? getEnvString('BENCH_PASSWORD', DEFAULT_BENCH_PASSWORD);
  const benchDisplayName =
    options.benchDisplayName ?? getEnvString('BENCH_DISPLAY_NAME', DEFAULT_BENCH_DISPLAY_NAME);
  const benchWorkspaceName =
    options.benchWorkspaceName ??
    getEnvString('BENCH_WORKSPACE_NAME', DEFAULT_BENCH_WORKSPACE_NAME);
  const targetTaskCount =
    options.targetTaskCount ?? getEnvInteger('BENCH_TASK_COUNT', DEFAULT_BENCH_TASK_COUNT);
  const taskPrefix = options.taskPrefix ?? getEnvString('BENCH_TASK_PREFIX', DEFAULT_BENCH_TASK_PREFIX);

  const auth = await registerOrLogin({
    apiBaseUrl,
    email: benchEmail,
    password: benchPassword,
    displayName: benchDisplayName,
    workspaceName: benchWorkspaceName,
  });

  const me = await requestJson(apiBaseUrl, '/auth/me', {
    headers: createAuthHeaders(auth.accessToken),
  });

  const workspace = me?.activeWorkspace ?? me?.workspaces?.[0];

  if (!workspace || typeof workspace.id !== 'string') {
    throw new Error('Unable to resolve benchmark workspace from /auth/me response.');
  }

  const workspaceId = workspace.id;
  const existingTasks = await listWorkspaceTasks(apiBaseUrl, workspaceId, auth.accessToken);
  const benchmarkTasks = existingTasks.filter((task) => isBenchmarkTask(task, taskPrefix));

  const tasksToCreate = Math.max(targetTaskCount - benchmarkTasks.length, 0);

  for (let index = 0; index < tasksToCreate; index += 1) {
    const title = `${taskPrefix} task ${benchmarkTasks.length + index + 1}`;
    await requestJson(apiBaseUrl, `/workspaces/${workspaceId}/tasks`, {
      method: 'POST',
      headers: createAuthHeaders(auth.accessToken),
      body: {
        title,
        description: 'Generated by benchmark fixture setup.',
      },
    });
  }

  const refreshedTasks = await listWorkspaceTasks(apiBaseUrl, workspaceId, auth.accessToken);
  const refreshedBenchmarkTasks = refreshedTasks.filter((task) => isBenchmarkTask(task, taskPrefix));

  if (refreshedBenchmarkTasks.length === 0) {
    throw new Error('Fixture setup completed, but no benchmark task was found.');
  }

  return {
    capturedAt: toIsoTimestamp(),
    apiBaseUrl,
    auth: {
      accessToken: auth.accessToken,
      userId: auth.userId,
    },
    workspaceId,
    sampleTaskId: refreshedBenchmarkTasks[0].id,
    taskPrefix,
    targetTaskCount,
    benchmarkTaskCount: refreshedBenchmarkTasks.length,
    createdTaskCount: tasksToCreate,
  };
}

async function registerOrLogin(input) {
  const registerBody = {
    email: input.email,
    password: input.password,
    displayName: input.displayName,
    workspaceName: input.workspaceName,
  };

  try {
    const registerResponse = await requestJson(input.apiBaseUrl, '/auth/register', {
      method: 'POST',
      body: registerBody,
    });

    return validateAuthPayload(registerResponse);
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);

    if (!message.includes('(409)')) {
      throw error;
    }

    const loginResponse = await requestJson(input.apiBaseUrl, '/auth/login', {
      method: 'POST',
      body: {
        email: input.email,
        password: input.password,
      },
    });

    return validateAuthPayload(loginResponse);
  }
}

async function listWorkspaceTasks(apiBaseUrl, workspaceId, accessToken) {
  const response = await requestJson(apiBaseUrl, `/workspaces/${workspaceId}/tasks`, {
    headers: createAuthHeaders(accessToken),
  });

  if (!response || typeof response !== 'object' || !Array.isArray(response.tasks)) {
    throw new Error('Unexpected response shape while listing workspace tasks.');
  }

  return response.tasks;
}

function isBenchmarkTask(task, taskPrefix) {
  return (
    task &&
    typeof task === 'object' &&
    typeof task.title === 'string' &&
    task.title.startsWith(taskPrefix) &&
    typeof task.id === 'string'
  );
}

function validateAuthPayload(value) {
  const accessToken = value?.accessToken;
  const userId = value?.user?.id;

  if (typeof accessToken !== 'string' || accessToken.length === 0) {
    throw new Error('Authentication response did not include an access token.');
  }

  if (typeof userId !== 'string' || userId.length === 0) {
    throw new Error('Authentication response did not include a user id.');
  }

  return {
    accessToken,
    userId,
  };
}

async function runFromCli() {
  const fixture = await prepareFixtures();

  const output = {
    capturedAt: fixture.capturedAt,
    apiBaseUrl: fixture.apiBaseUrl,
    workspaceId: fixture.workspaceId,
    sampleTaskId: fixture.sampleTaskId,
    taskPrefix: fixture.taskPrefix,
    targetTaskCount: fixture.targetTaskCount,
    benchmarkTaskCount: fixture.benchmarkTaskCount,
    createdTaskCount: fixture.createdTaskCount,
  };

  console.log(JSON.stringify(output, null, 2));
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  runFromCli().catch((error) => {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
  });
}
